with open(ROOT / "brands_today.json") as f:
    today = json.load(f)

with open(ROOT / "brands_done.json") as f:
    done = json.load(f)

fix_count = 0
for brand in today["brands"]:
    name = brand.get("name", "")
    if name in NEW_DESC:
        old_len = len(brand["desc"])
        brand["desc"] = NEW_DESC[name]
        new_len = len(brand["desc"])
        print(f"[{name}] {old_len}字 -> {new_len}字 OK")
        fix_count += 1

        # 匹配 brands_done
        slug = brand.get("slug", "")
        if slug in done["brands"]:
            done["brands"][slug]["desc"] = NEW_DESC[name]
        else:
            for sk, sv in done["brands"].items():
                dn = sv.get("name", "") or sv.get("name_cn", "")
                if dn.strip() == name.strip():
                    done["brands"][sk]["desc"] = NEW_DESC[name]
                    print(f"  -> brands_done[{sk}] updated")
                    break

with open(ROOT / "brands_today.json", "w", encoding="utf-8") as f:
    json.dump(today, f, ensure_ascii=False, indent=2)

with open(ROOT / "brands_done.json", "w", encoding="utf-8") as f:
    json.dump(done, f, ensure_ascii=False, indent=2)

print(f"Done: {fix_count} brands fixed")
